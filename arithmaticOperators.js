const x=12;
const y=3;


//ADDITION
console.log(x+y);

//SUBTRACTION
console.log(x-y);

//MULTIPLICATION
console.log(x*y);

//DIVISION
console.log(x/y);

//MODULUS
console.log(x%y);

//SQUARE, CUBE
console.log(x ** 2);
console.log(y ** 3);

